void Form_Logo_Draw(LPWindow pWindow)
{
	uint16 i;
	LPControl	lpControl;

	//清除显存
	EraseBuffer();

	//禁止刷屏
	EnableScreenFlush(FALSE);

	//禁止绘图
	SetRedraw(FALSE);

	//设置视图
	SetGdiView(pWindow->nViewPosX, pWindow->nViewPosY, pWindow->nViewSizeX, pWindow->nViewSizeY);
	EnableGdiView(TRUE);

	//绘制控件
	for(i = 0; i < pWindow->nNbControls; i++)
	{
		lpControl = *(pWindow->pLPControls + i);
		if(lpControl->state & CTRL_VISABLE)
		{
			lpControl->DrawFunc(lpControl);
		}
	}
	DrawGbText("重庆大唐科技股份有限公司", 36, 90);
	DrawGbText("正在初始化......", 64, 110);

	//使能刷屏
	EnableScreenFlush(TRUE);

	//刷屏
	FlushScreen();

	//使能绘图
	SetRedraw(TRUE);
}

void Form_Logo_Proc(LPWindow pWindow, LPGuiMsgInfo pGuiMsgInfo)
{
	
	switch(pGuiMsgInfo->ID)
	{
		case WM_LOAD:
			ClearScreen();
			SysTimeDly(15);
		case WM_SHOW:
			pWindow->DrawFunc(pWindow);
			break;
		
		default:
			break;
	}	
}

